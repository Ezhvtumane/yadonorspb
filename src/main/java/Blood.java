public class Blood {

    private BloodType bloodType;
    private BloodRh bloodRh;
    private BloodNeeds bloodNeeds;

    public BloodType getBloodType() {
        return bloodType;
    }

    public void setBloodType(BloodType bloodType) {
        this.bloodType = bloodType;
    }

    public BloodRh getBloodRh() {
        return bloodRh;
    }

    public void setBloodRh(BloodRh bloodRh) {
        this.bloodRh = bloodRh;
    }

    public BloodNeeds getBloodNeeds() {
        return bloodNeeds;
    }

    public void setBloodNeeds(BloodNeeds bloodNeeds) {
        this.bloodNeeds = bloodNeeds;
    }
}
